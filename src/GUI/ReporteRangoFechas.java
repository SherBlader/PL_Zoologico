    void imprimirRango(Date fech1, Date fech2)
    {
        Jaula p = ListaJaula.cabJau;
        int i = 0;
        java.text.DateFormat formato = DateFormat.getDateInstance();
        
        while (p != null)
        {           
            for (int h=0; h<p.listaA.size(); h++)
            {
                Animal a=null;
                a= (Animal)p.listaA.get(h);
                
                if (a.getFechaEntrada().compareTo(fech1) >= 0 && a.getFechaEntrada().compareTo(fech2) <= 0)
                {
                    model.insertRow(i,new Object[]{}); //INSERTA FILA EN TIEMPO DE EJECUCION
                    model.setValueAt(a.getNombreAnimal(), i, 0);  // ACTUALIZA LA CELDA CON EL VALOR DE CAMPO OBTENIDO                 
                    model.setValueAt(a.getIdAnimal(), i, 1);                 
                    model.setValueAt(a.getNombreCategoria(), i, 2); 
                    model.setValueAt(formato.format(a.getFechaEntrada()), i, 3);
                    i++;
                }
            }      
            p = p.sig;
        }
        
    }
    
