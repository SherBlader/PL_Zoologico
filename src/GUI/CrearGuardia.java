    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(!cedula.getText().isEmpty())
        {
          if(!nombre.getText().isEmpty())
          {
          if(!telefono.getText().isEmpty())
           {      
             if (!email.getText().isEmpty())
             {
                //Inserto guardia
                ListaGuardia.insertarFinal(new Guardia(cedula.getText().trim(), nombre.getText().trim(), telefono.getText().trim(), email.getText().trim(), true));
                JOptionPane.showMessageDialog(null, "Guardia Creado Exitosamente.........");  
                limpiar_campos();              
             }
             else
             {
                JOptionPane.showMessageDialog(null, "Campo Email es requerido");
                email.requestFocus();
             }
           }
           else
           {
            JOptionPane.showMessageDialog(null, "Campo Tel√©fono es requerido");
            telefono.requestFocus();
           }
          }
          else
          {
                JOptionPane.showMessageDialog(null, "Campo Nombre es requerido"); 
                nombre.requestFocus();
          }
        }
          else
        {
            JOptionPane.showMessageDialog(null, "Campo Cedula es requerido");
            cedula.requestFocus();       
        }      
        
    }//GEN-LAST:event_jButton2ActionPerformed

     public void limpiar_campos()
    {
        cedula.setText(null);
        nombre.setText(null);
        telefono.setText(null);
        email.setText(null);            
    }
   
