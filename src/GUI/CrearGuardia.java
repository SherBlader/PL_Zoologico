    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String ced =JOptionPane.showInputDialog("Digite La Cedula: " );
        //Busco el Guardia
        if(ListaGuardia.buscar(ced))
        {
            cedula.setText(ListaL_Guardia.aux.getCedula_guardia());//Tomo el auxiliar que tengo en ListaL_Guardia y modifico
            nombre.setText(ListaL_Guardia.aux.getNombre_guardia());
            telefono.setText(ListaL_Guardia.aux.getTelefono());
            email.setText(ListaL_Guardia.aux.getEmail());
            cedula.setEnabled(false);
            nombre.setEnabled(false);
   
        }
        else
            JOptionPane.showMessageDialog(null, "Guardia no Existe.......");  
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Modifico datos
        ListaL_Guardia.aux.setTelefono(telefono.getText().trim());
        ListaL_Guardia.aux.setEmail(email.getText().trim());
        limpiar_campos();
        cedula.setEnabled(true);
        nombre.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Modificado Correctamente"); 
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

