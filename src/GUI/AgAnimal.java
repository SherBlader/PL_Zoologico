    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!c1.getText().isEmpty())
        {
            int a1=categoriaCombo.getSelectedIndex();
            if (a1 != -1)
            {
                int a2=animalCombo.getSelectedIndex();
                if (a2 != -1)
                {
                    int a3=jaulaCombo.getSelectedIndex();
                    if (a3 != -1)
                    {
                        if (fechaEntrada.getDate() != null)
                        {
                            String seleccionCategoria = (String)categoriaCombo.getSelectedItem();
                            java.util.StringTokenizer st = new java.util.StringTokenizer(seleccionCategoria, "-");
                            String cat = st.nextToken();  //id Categoria                         
                            String nombre_categoria= st.nextToken();//Nombre Categoria

                            String seleccionAnimal = (String)animalCombo.getSelectedItem();//Nombre Animal

                            Long seleccionJaula = (Long)jaulaCombo.getSelectedItem();//idJaula

                            //Introduciendo Animal:

                            Jaula p = ListaJaula.cabJau;

                            while (p != null)
                            {
                                if (p.getIdJaula() == seleccionJaula)
                                {
                                    Animal a= new Animal();
                                    a.setIdAnimal(Long.parseLong(c1.getText().trim()));
                                    a.setCategoria(cat);//Id Categoria
                                    a.setNombreCategoria(nombre_categoria);
                                    a.setNombreAnimal(seleccionAnimal);
                                    a.setFechaEntrada(fechaEntrada.getDate());
                                    p.listaA.add(a);
                                    JOptionPane.showMessageDialog(null, "Animal AÃ±adido A Jaula Exitosamente...");
                                    refrescar();
                                    break;
                                }
                                p = p.sig;         
                            }   
                        }
                        else
                            JOptionPane.showMessageDialog(null, "Ingrese la Fecha de Entrada");
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Seleccione La Jaula");
                }
                else
                    JOptionPane.showMessageDialog(null, "Seleccione El Animal");
                
            }
            else
                JOptionPane.showMessageDialog(null, "Seleccione Categoria");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Escriba Id Animal");
            c1.requestFocus();
        }
            
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void categoriaComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categoriaComboActionPerformed


        //Para mostrar los animales en el jcombobox
        animalCombo.removeAllItems(); 

        String seleccion = (String)categoriaCombo.getSelectedItem();

        java.util.StringTokenizer st = new java.util.StringTokenizer(seleccion.trim(), "-");

        String token = (String) st.nextToken();
        long num= Long.parseLong(token);

        listadoComboAnimal(num);
      


    }//GEN-LAST:event_categoriaComboActionPerformed

    void refrescar()
    {
        c1.setText(null);
        fechaEntrada.setDate(null);
        categoriaCombo.setSelectedIndex(0);
        animalCombo.setSelectedIndex(0);
        jaulaCombo.setSelectedIndex(0);
    }
    
    //Metodo para cargar los animales en el jcombobox con el id de la categoria
    void listadoComboAnimal(long seleccion)
    {
        Categoria c = ListaCategoria.cabCat;
        
        while ( c != null)
        {
            if (c.getIdCategoria() == seleccion)
            {
                for (int j=0; j<c.animalesCategoria.size(); j++)
                {
                    String animal = (String)c.animalesCategoria.get(j);
                    animalCombo.addItem(animal);
                }
                break;
            }
            
            c = c.sig;
        }        
    }    
    
    
    
    
    
    
    
